public class Main {

    static int[] machines;
    static int target;

    public static void main(String[] args) throws IOException {

        Reader in = new Reader();
        int n = in.nextInt();
        int target = in.nextInt();
        int[] numbers = new int[n];
        for(int i = 0; i < n; i++){
            numbers[i] = in.nextInt();
        }

        Map<Integer, Integer> pairs = new HashMap<>(n);

        for(int i = 0; i < n; i++){
            pairs.put(numbers[i], i);
        }

        boolean found = false;

        for(int i = 0; i < n && !found; i++){
            for(int j = i + 1; j < n; j++){
                int key = target - (numbers[i] + numbers[j]);
                if(pairs.containsKey(key)){
                    int index = pairs.get(key);
                    if(index != i && index != j){
                        found = true;
                        System.out.print((i + 1) + " " + (j + 1) + " " + (index + 1));
                        break;
                    }
                }
            }
        }

        if(!found){
            System.out.print("IMPOSSIBLE");
        }
    }



    public static <T> void sort(T[] a, Comparator<T> comp){
        int gap = 0;
        while(gap < a.length) gap = (gap * 3) + 1;
        gap/= 3;

        while (gap > 0) {
            for (int i = gap; i < a.length; i += 1) {

                T temp = a[i];
                int j = i;

                while(j >= gap && comp.compare(a[j - gap],  temp) > 0){
                    a[j] = a[j - gap];
                    j -= gap;
                }
                a[j] = temp;
            }
            gap/= 3;
        }
    }
}

