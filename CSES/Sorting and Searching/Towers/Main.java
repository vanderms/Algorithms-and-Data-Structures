class Cube implements Comparable<Cube>{
    int size;
    int id;

    public Cube(int size, int id){
        this.size = size;
        this.id = id;
    }

    @Override
    public boolean equals(Object o){
        Cube other = (Cube) o;
        return this.id == other.id;
    }

    @Override
    public int compareTo(Cube o){
        return this.size != o.size ? this.size - o.size : o.id - this.id;
    }
}




