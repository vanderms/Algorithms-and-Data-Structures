import java.io.DataInputStream;
import java.io.FileInputStream;
import java.io.IOException;
import java.util.*;

public class Main {

    static int[] machines;
    static int target;

    public static void main(String[] args) throws IOException {

        Reader in = new Reader();
        int n = in.nextInt();
        Task[] tasks = new Task[n];

        for(int i = 0; i < n; i++){
            tasks[i] = new Task(in.nextInt(), in.nextInt());
        }

        sort(tasks, (x, y) -> x.duration - y.duration);

        long maximum = 0;
        long time = 0;

        for(int i = 0; i < n; i++){
            maximum += tasks[i].deadline - (tasks[i].duration + time);
            time+= tasks[i].duration;
        }

        System.out.print(maximum);
    }


    public static <T> void sort(T[] a, Comparator<T> comp){
        int gap = 0;
        while(gap < a.length) gap = (gap * 3) + 1;
        gap/= 3;

        while (gap > 0) {
            for (int i = gap; i < a.length; i += 1) {

                T temp = a[i];
                int j = i;

                while(j >= gap && comp.compare(a[j - gap],  temp) > 0){
                    a[j] = a[j - gap];
                    j -= gap;
                }
                a[j] = temp;
            }
            gap/= 3;
        }
    }
}


class Task{
    public int duration;
    public int deadline;
    public Task(int duration, int deadline){
        this.duration = duration;
        this.deadline = deadline;
    }
}




