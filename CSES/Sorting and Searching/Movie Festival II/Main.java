import java.io.DataInputStream;
import java.io.FileInputStream;
import java.io.IOException;
import java.util.*;

public class Main {


    public static void main(String[] args) throws IOException {

        Reader in = new Reader();
        int n = in.nextInt();
        int k = in.nextInt();
        Movie[] movies = new Movie[n];
        for(int i = 0; i < n; i++){
            movies[i] = new Movie(in.nextInt(), in.nextInt());
        }

        sort(movies, (x, y) -> x.end != y.end ? x.end - y.end : x.start - y.start);
        TreeMap<Integer, Integer> members = new TreeMap<>();
        members.put(0, k);

        int counter = 0;

        for(int i = 0; i < n; i++){
            Integer freeMember = members.lowerKey(movies[i].start + 1);
            if(freeMember != null){
                counter++;

                int quantity = members.get(freeMember);
                if(quantity > 1){
                    members.put(freeMember, quantity - 1);
                }
                else {
                    members.remove(freeMember);
                }

                if(members.containsKey(movies[i].end)){
                    members.put(movies[i].end, members.get(movies[i].end) + 1);
                }
                else {
                    members.put(movies[i].end, 1);
                }
            }
        }

        System.out.print(counter);

    }




   public static <T> void sort(T[] a, Comparator<T> comp){
        int gap = 0;
        while(gap < a.length) gap = (gap * 3) + 1;
        gap/= 3;

        while (gap > 0) {
            for (int i = gap; i < a.length; i += 1) {

                T temp = a[i];
                int j = i;

                while(j >= gap && comp.compare(a[j - gap],  temp) > 0){
                    a[j] = a[j - gap];
                    j -= gap;
                }
                a[j] = temp;
            }
            gap/= 3;
        }
   }
}


class Movie {
    protected int start;
    protected int end;

    public Movie(int start, int end) {
        this.start = start;
        this.end = end;
    }
}


